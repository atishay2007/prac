a = 10
b = 5

print("Sum:", a + b)
print("Difference:", a - b)
print("Product:", a * b)
print("Division:", a / b)
print("Integer Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)
print("Complex Operation:", (a + b) * (a - b) / b)
print("Floor of Division:", a // b)
print("Ceiling of Division:", -(-a // b))  # Ceiling using negative floor
print("Rounded Division:", round(a / b))
print("Absolute Difference:", abs(a - b)) 
print("Power of 2:", pow(a, 2))
print("Square Root of a:", a ** 0.5)
print("Square Root of b:", b ** 0.5)
print("Max of a and b:", max(a, b))
print("Min of a and b:", min(a, b))
print("Average of a and b:", (a + b) / 2)
print("Geometric Mean of a and b:", (a * b) ** 0.5)
print("Harmonic Mean of a and b:", 2 * a * b / (a + b))  # Harmonic mean formula

